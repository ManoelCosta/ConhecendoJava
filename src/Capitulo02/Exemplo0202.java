package Capitulo02;
/** Exemplo0202:
    Essa classe demonstra a utilização de variáveis em uma classe em Java.
    São declaradas variáveis do tipo int e double.
    O exemplo tabém demonstra como imprimir o conteudo das variáveis na tela
 */
public class Exemplo0202 {
    public static void main(String args[]){
        int x = 10, y = 20; // declaração de variáveis do tipo inteiro
        double dolar = 2.62; // declaração de variáveis do tipo double
        /*as linhas seguintes enviam o
        conteúdo das variáveis para a tela
        */
        System.out.println(x);
        System.out.println(y);
        System.out.println(dolar);
    }
}
